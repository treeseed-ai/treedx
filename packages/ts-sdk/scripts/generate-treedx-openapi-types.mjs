import fs from "node:fs";
import path from "node:path";
import YAML from "yaml";

const packageRoot = path.resolve(import.meta.dirname, "..");
const repoRoot = path.resolve(packageRoot, "..", "..");
const openapiPath = path.join(repoRoot, "docs", "api", "openapi.yaml");
const outputPath = path.join(packageRoot, "src", "treedx", "generated", "openapi-types.ts");

export function renderOpenApiTypes() {
  const openapi = YAML.parse(fs.readFileSync(openapiPath, "utf8"));
  const operations = [];

  for (const [apiPath, methods] of Object.entries(openapi.paths ?? {})) {
    if (!apiPath.startsWith("/api/v1/")) {
      continue;
    }
    for (const method of Object.keys(methods)) {
      operations.push({ method: method.toUpperCase(), path: apiPath });
    }
  }

  operations.sort((a, b) => `${a.method} ${a.path}`.localeCompare(`${b.method} ${b.path}`));
  const operationLiterals = operations.map((operation) => `  { method: "${operation.method}", path: "${operation.path}" }`).join(",\n");

  return `// Generated by scripts/generate-treedx-openapi-types.mjs. Do not edit by hand.\n\nexport type TreeDxOpenApiMethod = "GET" | "POST" | "PUT" | "PATCH" | "DELETE";\n\nexport interface TreeDxOpenApiOperation {\n  method: TreeDxOpenApiMethod;\n  path: string;\n}\n\nexport type TreeDxOpenApiPath = typeof TREEDX_OPENAPI_OPERATIONS[number]["path"];\nexport type TreeDxOpenApiOperationKey = \`${"${TreeDxOpenApiMethod} ${TreeDxOpenApiPath}"}\`;\n\nexport const TREEDX_OPENAPI_OPERATION_COUNT = ${operations.length} as const;\n\nexport const TREEDX_OPENAPI_OPERATIONS = [\n${operationLiterals}\n] as const satisfies readonly TreeDxOpenApiOperation[];\n`;
}

if (process.argv[1] && path.resolve(process.argv[1]) === new URL(import.meta.url).pathname) {
  fs.writeFileSync(outputPath, renderOpenApiTypes(), "utf8");
}
